    public void populate(){
        DefaultTableModel dtm = (DefaultTableModel)tblDirectory.getModel();
        dtm.setRowCount(0);
        
        for(Fleet a : fleeDir.getFleetDirectory()){
            if(a.getAirname().equalsIgnoreCase(airLiner.getAirlinerName()))
            {
            Object[] row  = new Object[dtm.getColumnCount()];
            row[0]=a;
            row[1]=a.getAirname();
            row[2]=a.getTotalSeats();
            dtm.addRow(row);
        }
    }
    }
