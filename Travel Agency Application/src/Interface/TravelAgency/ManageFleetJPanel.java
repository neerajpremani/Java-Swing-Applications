    private void deleteFleetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteFleetBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDirectory.getSelectedRow();
        if(selectedRow>=0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                Fleet fleet = (Fleet)tblDirectory.getValueAt(selectedRow, 0);
                fleeDir.deleteFleet(fleet);
                populate();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_deleteFleetBtnActionPerformed

    private void viewFleetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewFleetBtnActionPerformed
        // TODO add your handling code here:
        
        int selectRow = tblDirectory.getSelectedRow();
        if(selectRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row");
        }
        else{
            Fleet fleet = (Fleet) tblDirectory.getValueAt(selectRow, 0);
            ViewFleetJPanel vfPanel = new ViewFleetJPanel(rightPanel, fleet, fleeDir);
            rightPanel.add("ViewFleetJPanel", vfPanel);
            CardLayout layout= (CardLayout) rightPanel.getLayout();
            layout.next(rightPanel);
        }
    }//GEN-LAST:event_viewFleetBtnActionPerformed


